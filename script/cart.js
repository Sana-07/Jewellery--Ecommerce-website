    const cartItems = JSON.parse(localStorage.getItem("productList")) || [];

const cartTbody = document.getElementById('cart');


function renderCartItems() {
  cartTbody.innerHTML = '';
  cartItems.forEach((cartItem, index) => {
    const product = products.find(product => product.id == cartItem.id);

    if (product) {
      const row = document.createElement('tr');
      const totalPrice = product.price * cartItem.quantity;

      
      const displayIndex = index + 1;

      row.innerHTML = `
        <td>${displayIndex}</td>
        <td><img src="${product.img}" alt="" class="img" width="100px"></td>
        <td class="title">${product.title}</td>
        <td><input type="number" value="${cartItem.quantity}" min="1" onchange="updateQuantity(${index}, this.value)"></td>
        <td>Rs <span>${totalPrice.toFixed(2)}</span></td> 
        <td><button onclick="removeItem(${index})">Remove</button></td>
      `;
      cartTbody.appendChild(row);
    }
  });
}


function updateQuantity(index, newQuantity) {
  
  cartItems[index].quantity = parseInt(newQuantity);


  localStorage.setItem('productList', JSON.stringify(cartItems));

  
  renderCartItems();

}


function removeItem(index) {
  
  cartItems.splice(index, 1);

  
  localStorage.setItem('productList', JSON.stringify(cartItems));

   
  renderCartItems();
  alert("Item removed sucessfully")

  window.location.reload()
}


renderCartItems();
document.getElementById('quantity').innerHTML = `(${cartItems.length})`;
    
  

